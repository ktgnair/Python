# Logical Operators

# AND operator - if both the conditions are true then the statement is true else its false 
a = 10
print(a > 10 and a < 10)
print(a < 11 and a < 12)

# OR operator - if any one condition is true then the statement is true else rest all is false
print(a > 10 or a < 12)

# NOT operator - if condition is true the using not it will give false vice versa
print(not a < 11)